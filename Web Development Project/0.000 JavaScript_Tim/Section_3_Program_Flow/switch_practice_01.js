let num = 33;

//switch structure
switch (num) {
    case 1:
        console.log("The number is 1");
    break;
    case 2:
        console.log("Its 2!")
    break;
    case 3:
    console.log("Its 3!")
    break;
    case 4:
        console.log("Its 4!")
    break;
    default:
        console.log(`Its ${num}`)
}